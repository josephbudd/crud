// Do dispatches local procedure calls from the renderer to the main process handlers here in package dispatch.
func Do(cargo interface{}, sending lpc.Sending, eojing lpc.EOJer, stores *store.Stores) {
	switch cargo := cargo.(type) {
	case *message.LogRendererToMainProcess:
		// Log does not need a lpc.EOJer because it does not have a go routine.
		handleLog(cargo, sending)
	case *message.GetRemoveSelectContactsPageRendererToMainProcess:
		handleGetRemoveSelectContactsPage(cargo, sending, eojing, stores)
	case *message.GetEditSelectContactsPageRendererToMainProcess:
		handleGetEditSelectContactsPage(cargo, sending, eojing, stores)
	case *message.GetRemoveContactRendererToMainProcess:
		handleGetRemoveContact(cargo, sending, eojing, stores)
	case *message.AddContactRendererToMainProcess:
		handleAddContact(cargo, sending, eojing, stores)
	case *message.ReloadContactsRendererToMainProcess:
		handleReloadContacts(cargo, sending, eojing, stores)
	case *message.RemoveContactRendererToMainProcess:
		handleRemoveContact(cargo, sending, eojing, stores)
	case *message.GetEditContactRendererToMainProcess:
		handleGetEditContact(cargo, sending, eojing, stores)
	case *message.EditContactRendererToMainProcess:
		handleEditContact(cargo, sending, eojing, stores)
	default:
		log.Println("dispatch Do: unknown cargo type.")
	}
}
